<h2 class="hcls">Paper Submission</h2>


<h2><a name="acceptance">Instructions</a></h2>

<p>
Papers should describe original and previously unpublished research. Three metrics will be applied to judge the submissions: (a) Relevance of the problem for development; (b) Novelty of the ideas; (c) Evaluation of the solution, making a case for development-focused impact. All DEV paper submissions should either provide or directly motivate a novel technical solution that has direct implications for development.
</p>

<p>
Full papers must not exceed 9 pages and Short papers on Practical Experiences must not exceed 2 pages.  All Submissions must be in ACM format, submitted as a pdf.  The template for submissions can be found <a href="http://www.acm.org/sigs/publications/proceedings-templates">here</a>. 
</p>

<p>Submissions should be fully anonymized. In particular, the names or affiliations of authors should not be included so as to allow for blind review. Papers should be submitted via the submission site. As part of the submission process, please indicate one or more tracks for which your submission is best suited.
</p>


<p>
	Then use the <a href="http://papers.acmdev.org/">paper submission site</a> to:
</p>

<ul>
	<li> Submit your paper by <b>Sunday July 19, 11:59pm, Pacific Standard Time (PST)</b>. Be sure to finalize your paper on the paper submission site to make it available for review. </li>
</ul>

<p>
This is a hard deadline, and no extensions will be given.
</p>

<p>
If your paper is accepted, we will provide further information for the camera-ready version.
</p>

<p>
If you have any questions about submitting papers to ACM DEV 2015, or encounter problems with the paper submission site, <a href="mailto:dev15pcchairs@gmail.com">contact the TPC chairs</a> well before the deadline.
</p>


